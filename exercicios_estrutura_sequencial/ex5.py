# Faça um Programa que converta metros para centímetros

metros_usario = input('Digite quantos metros deseja converter para centimetros: ')

metros_usario_int = int(metros_usario)

converter_metros_em_centimetro = metros_usario_int * 100

print(f'{metros_usario} metros convertido em centimetros são: {converter_metros_em_centimetro}cm')

